                    <p class="pt-4">BPS Provinsi Sulawesi Tenggara mencanangkan Eco Office dalam setiap kegiatan perkantoran sehari-hari. Pemberlakuan Eco office ini lingkungan tersebut akan berdampak pada semakin efisiennya pengeluaran-pengeluaran rutin yang secara langsung dapat berdampak secara ekonomis.</p>
                    <p>
                    Perilaku Eco Office ini diantaranya adalah mematikan lampu yang tidak digunakan dan memaksimalkan pencahayaan matahari, mematikan komputer dan peralatan elektronik yang tidak digunakan, menggunakan air seperlunya, pemanfaatan kertas bekas untuk mencatak laporan/publikasi, dan pembiasaan-pembiasaan perilaku peduli lingkungan lainnya yang mencerminkan peduli dengan lingkungan.
                    </p>
                    <p>
                    Perilaku peduli lingkungan ini akan terus menerus dikampanyekan kepada seluruh pegawai, sehingga akan menjadi kebiasaaan dan menjadi gaya hidup peduli lingkungan sebagai kontribusi kecil untuk menjaga lingkungan yang lebih besar.
